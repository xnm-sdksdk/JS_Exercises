/*
Iterate over a cycle which will iterate from 0 to 15,
for each iteration will check if them number is even or odd,
output them in the console
*/

for (let i = 0; i <= 15; i++) {
    if (i % 2 === 0) {
        console.log(i + " is even")
    } else {
        console.log(i + " is odd");
    }
}


